import os
import json
import glob

import streamlit as st

import dataset_manager as dm
import geo_reference as geo
import nav

st.set_page_config(layout="wide", page_title="Datasets")
nav.render_sidebar()
st.title("🗂️ Datasets")
st.markdown(
    "Pick which dataset the whole app works on, or add your own. Each dataset keeps its **own** "
    "inputs, lane geometry, site geometry, **georeferencing + HD-map**, registered cloud, background "
    "model, outputs and settings in a separate workspace — switching never overwrites another "
    "dataset's work."
)

active = dm.get_active()
st.success(f"**Active dataset:** {active.name}   ·   `{active.id}`")


def _pcd_count(p):
    try:
        return len(glob.glob(os.path.join(p, "*.pcd")))
    except Exception:
        return 0


def _gt_count(p):
    try:
        return len(glob.glob(os.path.join(p or "", "*.json")))
    except Exception:
        return 0


def _clear_session():
    for k in dm.SESSION_KEYS_TO_CLEAR:
        st.session_state.pop(k, None)


# ---------------- Select ----------------
st.subheader("Select a dataset")
datasets = dm.all_datasets()
ids = [d.id for d in datasets]


def _label(d):
    s = d.status()
    chips = ["🟢 PCDs" if s["pcd"] else "⚪ no PCDs"]
    if s["gt"]:
        chips.append("🏷️ GT")
    if s["lanes"]:
        chips.append("🛣️ lanes")
    if s["model"]:
        chips.append("📦 model")
    if s["filtered"]:
        chips.append("✨ filtered")
    return f"{d.name}{'  ·  template' if d.is_template else ''}  —  {' · '.join(chips)}"


choice = st.radio("Datasets", ids, index=ids.index(active.id) if active.id in ids else 0,
                  format_func=lambda i: _label(dm.get_dataset(i)), label_visibility="collapsed")
ds = dm.get_dataset(choice)

if choice != active.id:
    if st.button(f"✅ Switch to “{ds.name}” (clears in-memory results)", type="primary"):
        dm.set_active(choice)
        _clear_session()
        st.rerun()

# ---------------- Active/selected dataset detail (collapsible) ----------------
st.divider()
with st.expander(f"📋 {ds.name} — details", expanded=False):
    m1, m2, m3, m4 = st.columns(4)
    m1.metric("PCD frames", _pcd_count(ds.pcd_dir))
    m2.metric("GT label files", _gt_count(ds.gt_dir))
    m3.metric("Type", "template" if ds.is_template else "user")
    m4.metric("Created", (ds.d.get("created", "—") or "—").split("T")[0])
    if ds.d.get("description"):
        st.caption(ds.d["description"])

    # st.expander can't nest, so the per-topic detail panels are tabs.
    t_paths, t_tree, t_site, t_geo, t_lanes, t_manage = st.tabs(
        ["📁 Paths", "🌳 Structure", "🗺️ Site geometry", "🛰️ Georeferencing", "🛣️ Lanes", "✏️ Manage"])

    with t_paths:
        st.markdown("**Raw inputs** (the untouched download)")
        st.json({
            "south LiDAR clouds": ds.raw_lidar_south_dir,
            "north LiDAR clouds": ds.raw_lidar_north_dir,
            "south labels": ds.raw_labels_south_dir,
            "north labels": ds.raw_labels_north_dir,
            "camera images": ds.images_dir,
        })
        st.markdown("**Config** (tracked in git)")
        st.json({
            "lane geometry": ds.lanes_path,
            "site geometry": ds.site_geometry_path,
            "georeferencing": os.path.join(ds.config_dir, "georef.json"),
            "factory defaults": ds.defaults_dir,
            "HD-map (digital twin)": ds.hdmap_path,
        })
        st.markdown("**Derived + outputs** (generated by the app — see **Dataset Prep**)")
        st.json({
            "registered (fused south+north)": ds.registered_dir,
            "detection input (cropped)": ds.pcd_dir,
            "evaluation GT (visible-only)": ds.gt_dir,
            "background model": ds.model_path,
            "filtered clouds": ds.filtered_dir,
            "detection outputs": ds.detection_dir,
        })
        if not ds.status()["pcd"]:
            st.warning(f"No .pcd files found in `{ds.pcd_dir}`.")
            if ds.is_template:
                st.info("Template **config** ships with the repo, but its large LiDAR/image **data** "
                        "does not. Download the dataset and place the frames under "
                        f"`{os.path.join(ds.workspace, 'data')}` (see that folder's README). "
                        "You don't edit any paths — the template already points here.")

    with t_tree:
        st.code(
            f"""datasets/{ds.id}/
├── config/                          # tracked in git (small)
│   ├── lanes.geojson                # WWD lane directions (Lane Editor)
│   ├── site_geometry.json           # road / research / exclusion geometry
│   ├── georef.json                  # site name + map→cloud transforms (georeferencing)
│   └── defaults/                    # factory-default snapshots (reset-to-default)
├── map/                             # gitignored — HD-map road network (dev-kit)
│   └── lane_samples.json            # digital-twin lanes + WGS84 geoReference anchor
├── data/                            # gitignored (large, local)
│   ├── raw/                         # the untouched TUM Traffic download
│   │   ├── point_clouds/{{s110_lidar_ouster_south, _north}}/*.pcd
│   │   ├── labels/{{s110_lidar_ouster_south, _north}}/*.json
│   │   └── images/<camera>/*.jpg
│   └── derived/                     # regenerated in-app (Dataset Prep)
│       ├── point_clouds/registered/*.pcd            # fused south+north (south LiDAR frame)
│       ├── point_clouds/cropped/<sensor>/*.pcd      # clipped to road (pipeline input)
│       └── labels/scorable/<sensor>/*.json          # visible-only GT
└── outputs/                         # generated — gitignored
    ├── background/{{background_model, background_filtering}}/<sensor>/<crop>/
    ├── detection/object_detection/<sensor>/<crop>/   # tracks.csv, animation, eval report
    └── visualizer/{{rendered, road_videos}}/""",
            language="text")
        st.caption("`config/` is tracked in git; `map/`, `data/` (raw + derived) and `outputs/` stay "
                   "local per dataset. Everything under `derived/` can be regenerated from `raw/` on "
                   "the Dataset Prep page.")

    with t_site:
        st.caption("The processed region used by **Background Filtering** / cropping (from "
                   "`site_geometry.json`): the **research polygon** (area analysed), **road polygon(s)** "
                   "(drivable area), and **exclusion rectangles** (regions always ignored). Coordinates "
                   "are in the sensor frame (metres); edit them on **Dataset Prep → Geometry Editor**.")
        site_geo = None
        if os.path.exists(ds.site_geometry_path):
            try:
                site_geo = json.load(open(ds.site_geometry_path))
            except Exception:
                pass
        if site_geo and site_geo.get("research_polygon"):
            rp = site_geo["research_polygon"]
            roads = site_geo.get("road_polygons", [])
            rects = site_geo.get("foreground_exclusion_rects", [])
            xs = [p[0] for p in rp]; ys = [p[1] for p in rp]
            st.write(f"Research polygon bounds: X [{min(xs):.1f}, {max(xs):.1f}] · "
                     f"Y [{min(ys):.1f}, {max(ys):.1f}] · {len(roads)} road polygon(s) · "
                     f"{len(rects)} exclusion rect(s)")

            def _coord_rows(poly):
                return [{"#": i + 1, "X (m)": round(float(p[0]), 2), "Y (m)": round(float(p[1]), 2)}
                        for i, p in enumerate(poly)]

            if st.toggle("📐 Show coordinates", key=f"geo_coords_{ds.id}"):
                ct_res, ct_road, ct_excl = st.tabs(
                    ["🟦 Research polygon", f"🛣️ Road polygons ({len(roads)})",
                     f"⛔ Exclusion rects ({len(rects)})"])
                with ct_res:
                    st.dataframe(_coord_rows(rp), use_container_width=True, hide_index=True)
                with ct_road:
                    if roads:
                        for i, poly in enumerate(roads):
                            st.markdown(f"**Road polygon {i + 1}** · {len(poly)} vertices")
                            st.dataframe(_coord_rows(poly), use_container_width=True, hide_index=True)
                    else:
                        st.caption("None defined.")
                with ct_excl:
                    if rects:
                        summary = []
                        for i, r in enumerate(rects):
                            rx = [p[0] for p in r]; ry = [p[1] for p in r]
                            summary.append({"rect": i + 1,
                                            "x_min": round(min(rx), 2), "x_max": round(max(rx), 2),
                                            "y_min": round(min(ry), 2), "y_max": round(max(ry), 2),
                                            "w (m)": round(max(rx) - min(rx), 2),
                                            "h (m)": round(max(ry) - min(ry), 2)})
                        st.dataframe(summary, use_container_width=True, hide_index=True)
                        st.caption("Axis-aligned rectangles — the always-ignored foreground regions.")
                    else:
                        st.caption("None defined.")
        else:
            st.info("No site_geometry.json — the pipeline falls back to the built-in default geometry.")

        if not ds.is_template:
            c1, c2 = st.columns(2)
            if c1.button("🔄 Re-derive geometry from this dataset's PCDs"):
                geo2 = dm.derive_site_geometry(ds.pcd_dir)
                os.makedirs(ds.config_dir, exist_ok=True)
                json.dump(geo2, open(ds.site_geometry_path, "w"), indent=2)
                st.success("Re-derived research region from the data extent.")
                st.rerun()
            st.caption("Tip: build lanes for this dataset on the **Lane Editor** page (it saves to this "
                       "dataset's config), then run Background Filtering → Detection.")
        else:
            st.caption("Template geometry is curated and read-only here.")

    with t_geo:
        st.caption("Georeferencing places this dataset's sensor frame on the real Earth — used by the "
                   "**WWD Simulator** (true compass bearings + exact lat/lon V2X) and the **Visualizer** "
                   "(HD-map digital twin + Real-intersection map).")
        gref_path = os.path.join(ds.config_dir, "georef.json")
        gref = None
        if os.path.exists(gref_path):
            try:
                gref = json.load(open(gref_path))
            except Exception:
                pass
        if gref:
            st.success(f"Per-dataset `georef.json` found — overrides the built-in defaults.")
            cfg = gref
        else:
            st.info("No per-dataset `georef.json` — using the built-in TUMTraf defaults. Add one to "
                    "retarget the georeferencing/HD-map stack to another site (no code changes).")
            cfg = geo._DEFAULT_GEOREF

        if cfg.get("site_name"):
            st.markdown(f"**Site:** {cfg['site_name']}")
        ll = cfg.get("site_latlon")
        if ll:
            lat, lon = float(ll[0]), float(ll[1])
            gc1, gc2, gc3 = st.columns([1, 1, 2])
            gc1.metric("Latitude", f"{lat:.5f}°")
            gc2.metric("Longitude", f"{lon:.5f}°")
            gc3.write("")
            gc3.markdown(f"[🌍 Open in Google Maps](https://maps.google.com/?q={lat},{lon})")

        # capability status
        hd_ok = os.path.exists(ds.hdmap_path)
        try:
            import pyproj  # noqa: F401
            pyproj_ok = True
        except Exception:
            pyproj_ok = False
        st.write(f"HD-map road network: {'✅ present' if hd_ok else '⚪ missing'} · `{ds.hdmap_path}`")
        st.write(f"Exact WGS84 mode (pyproj): "
                 f"{'✅ available — exact lat/lon & geodesic bearings' if pyproj_ok else '⚪ unavailable — approximate placement'}")

        if st.toggle("🔧 Show transforms (HD-map → s110_base → cloud)", key=f"geo_tf_{ds.id}"):
            st.caption("Lifted verbatim from the TUMTraf dev-kit. `map_to_base` is a rigid "
                       "rotation+translation; `base_to_cloud` is a Z-rotation (degrees) + translation "
                       "into the point-cloud frame.")
            st.json({"map_to_base": cfg.get("map_to_base", {}),
                     "base_to_cloud": cfg.get("base_to_cloud", {})})

    with t_lanes:
        st.caption("**Lanes** (`lanes.geojson`) define the expected legal travel direction per road "
                   "region, used by **wrong-way detection**. Build/edit these on the **Lane Editor** page.")
        lanes_geo = None
        if os.path.exists(ds.lanes_path):
            try:
                lanes_geo = json.load(open(ds.lanes_path))
            except Exception:
                pass
        feats = (lanes_geo or {}).get("features", [])
        if feats:
            rows = []
            for f in feats:
                p = f.get("properties", {})
                try:
                    ring = f["geometry"]["coordinates"][0]
                    xs = [c[0] for c in ring]; ys = [c[1] for c in ring]
                    bounds = f"X[{min(xs):.0f},{max(xs):.0f}] Y[{min(ys):.0f},{max(ys):.0f}]"
                except Exception:
                    bounds = "—"
                rows.append({"lane": p.get("lane_id", "?"),
                             "heading°": round(float(p.get("heading_deg", 0)), 1),
                             "calibrated": "✅" if p.get("calibrated") else "—",
                             "bounds": bounds})
            st.dataframe(rows, use_container_width=True, hide_index=True)
            n_cal = sum(1 for f in feats if f.get("properties", {}).get("calibrated"))
            st.caption(f"{len(feats)} lane(s) · {n_cal} calibrated.")
        else:
            st.info("No lanes defined yet — create them on the **Lane Editor** page "
                    "(WWD is disabled until lanes exist).")

    with t_manage:
        if ds.is_template:
            st.caption("This is a built-in **template** — read-only here. Add your own dataset below "
                       "to rename or remove it.")
        else:
            new_name = st.text_input("Rename", value=ds.name, key="rename_field")
            rc1, rc2 = st.columns(2)
            if rc1.button("Save name"):
                dm.rename_dataset(ds.id, new_name)
                st.rerun()
            if rc2.button(f"🗑️ Remove “{ds.name}” (keeps its files on disk)"):
                dm.delete_dataset(ds.id)
                _clear_session()
                st.rerun()

# ---------------- Add a dataset (collapsible) ----------------
st.divider()
with st.expander("➕ Add your own dataset", expanded=False):
    st.caption("Point the app at a folder of `.pcd` frames on disk (not copied). A workspace for its "
               "config/model/outputs is created under `datasets/<id>/`, and a starter site geometry is "
               "derived from the data so it runs right away.")
    with st.form("new_dataset"):
        name = st.text_input("Dataset name", placeholder="e.g. My Intersection — North Camera")
        pcd_dir = st.text_input("Path to the folder of PCD frames", placeholder="/path/to/pcd_frames")
        gt_dir = st.text_input("Path to ground-truth labels (optional, OpenLABEL .json)", placeholder="")
        description = st.text_input("Description (optional)", placeholder="sensor, location, notes…")
        submitted = st.form_submit_button("Create dataset", type="primary")
        if submitted:
            if not name.strip():
                st.error("Please enter a name.")
            elif not pcd_dir.strip() or not os.path.isdir(pcd_dir.strip()):
                st.error(f"PCD folder not found: {pcd_dir}")
            else:
                n_pcd = _pcd_count(pcd_dir.strip())
                new = dm.create_dataset(name, pcd_dir, gt_dir, description)
                _clear_session()
                st.success(f"Created **{new.name}** (`{new.id}`) with {n_pcd} PCD frames and set it active. "
                           f"Starter geometry written to `{new.site_geometry_path}`.")
                if n_pcd == 0:
                    st.warning("No .pcd files were found in that folder — double-check the path.")
                st.rerun()
